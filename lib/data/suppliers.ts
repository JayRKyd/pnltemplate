// Search predefined suppliers by name or CUI
export function searchPredefinedSuppliers(query: string): PredefinedSupplier[] {
  if (!query || query.length < 2) {
    return [];
  }

  const lowerQuery = query.toLowerCase().trim();

  return predefinedSuppliers.filter(supplier => {
    const nameMatch = supplier.name.toLowerCase().includes(lowerQuery);
    const cuiMatch = supplier.cui && supplier.cui.includes(query.replace(/\s/g, ""));
    return nameMatch || cuiMatch;
  });
}
